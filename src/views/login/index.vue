<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">
          <img src="@/assets/common/login-logo.png" alt="">
        </h3>
      </div>

      <el-form-item prop="mobile">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="mobile"
          v-model="loginForm.mobile"
          placeholder="输入手机号"
          name="mobile"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="输入密码"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button class="loginBtn" :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">登入</el-button> <el-button class="loginBtn" :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="dleLogin">获取用户信息</el-button>

      <div class="tips">
        <span style="margin-right:20px;">账号: 13800000002</span>
        <span> 密码: 123456</span>
      </div>

    </el-form>
  </div>
</template>

<script>
import { validmobile } from '@/utils/validate'
// 导入发请求的方法
import { login } from '@/api/user'
export default {
  name: 'Login',
  data() {
    // value是里面的值账号密码，把这些传递过去用正则验证，然后返回结果正则不通过提醒用户，通过就下一步
    const validatemobile = (rule, value, callback) => {
      if (!validmobile(value)) {
        callback(new Error('格式错误'))
      } else {
        callback()
      }
    }
    // const validatePassword = (rule, value, callback) => {
    //   if (value.length < 6) {
    //     callback(new Error('The password can not be less than 6 digits'))
    //   } else {
    //     callback()
    //   }
    // }
    return {
      loginForm: {
        mobile: '13800000002',
        password: '123456'
      },
      loginRules: {
        mobile: [
          { required: true, message: '手机号不能为空', trigger: ['change', 'blur'] },
          { validator: validatemobile, trigger: ['change', 'blur'] }

        ],
        password: [
          { required: true, message: '密码不能为空', trigger: ['change', 'blur'] },
          { min: 6, max: 16, message: '密码应为六到十六位', trigger: ['change', 'blur'] }
        ]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        this.doLogin()
      })
    },
    async doLogin() {
      try {
        // 定义一个方法，然后触发上面的方法,把账号密码带入axios请求验证是否正确
        const res = await login(this.loginForm)
        this.$message.success(res.message)
        console.log(res.data)
        // 调用User里面的方法并把获取的Token传进去，让Vuex里面的Token=我传递过去的Token
        this.$store.commit('user/setToken', res.data)

        // 跳转到首页
        this.$router.push(this.$route.query.return_url || '/')
      } catch (err) {
        // console.log(err)
        this.$message.error(err.message)
      }
    }
    // async dleLogin() { // 测试获取用户信息
    //   const { data: res } = await getInfo()
    //   // console.log(res)
    // }
  }
}
</script>

